				<h2>Доступ</h2>
				<p>Программа запускается через интернет-браузер. Для использования программы необходимо зарегистироваться. Вход в программу осуществляется по указанному при регистрации адресу электронной почты и паролю. Выход - по нажатию соответствующей кнопки на главной странице программы. В течение первых трех дней с момента регистрации пользователю бесплатно предоставляется полный функционал программы. Дальнейший доступ к программе осуществляется на возмездной основе путем внесения ежемесячной платы за услуги по использованию программы. Вырученные средства направлены на покрытие расходов, связанных с работой программы.</p>
