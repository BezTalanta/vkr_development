				<h2>Конфиденциальность</h2>
				<p>Программа не хранит персональные данные пациентов и медицинских работников. При регистрации пользователя программы достаточно указать существующий адрес электронной почты, на который в течение суток придет письмо со ссылкой для подтверждения регистрации с последующим вводом пароля для входа. Пароли хранятся в зашифрованном виде. В случае утери пароля на электронную почту зарегистрированного пользователя по его запросу высылается ссылка для ввода нового пароля. Данные, вводимые в поля формы, обезличены. Ответственность за содержание полей и за введение персональных данных пациента в какое-либо поле лежит целиком на пользователе программы. Данные, введенные каким-либо пользователем программы, недоступны другим пользователям программы и пользователям интернет. Все введенные данные хранятся в открытом виде. 
			</div>				
